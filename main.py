first_direction = input('You are at a crossroad. Which way should you go? Left or Right? ')
if first_direction.lower() == 'right':
	print('You have fallen into a hole. Game over.')	
if first_direction.lower() == 'left':
	second_direction = input('You have come across a lake and there is a small island in the middle of it. Do you want to swim or wait? ')
	if second_direction.lower() == 'swim':
		print('You were attacked by a trout. Game over.')
	if second_direction.lower() == 'wait':
		third_direction = input('You have come upon a house with three doors. Each door is a different color: red, blue, and yellow. ')
		if third_direction.lower() == 'red':
			print('You were burned by fire. Game Over.')
		elif third_direction.lower() == 'blue':
			print('You were eaten by beasts. Game Over.')
		elif third_direction.lower() == 'yellow':
			print('You Win!')
		else: 
			print('Game Over.')